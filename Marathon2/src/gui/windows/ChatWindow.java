package gui.windows;

import java.util.Set;

import message.Message;

/**
 * Interface for chat windows, also implemented by history screen.
 */
public interface ChatWindow {
	
	/**
	 * Display a message.
	 * 
	 * @param message
	 */
	public void displayMessage(Message message);
	
	/**
	 * Get members of chat.
	 * 
	 * @return
	 */
	public Set<String> getMembers();
	
	/**
	 * Close window.
	 */
	public void close();
	
}
